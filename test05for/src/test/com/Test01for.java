package test.com;

public class Test01for {
	public static void main(String[] args) {
		
		
		//정해진 횟수,길이만큼 반복: for
		for (int i = 98; i >0; i--) {
			System.out.println("for...."+i);
		}
		for (byte i = 0; i < 5; i++) {
			System.out.println("for...."+i);
		}
		for (char i = 0; i < 5; i++) {
			System.out.println("for...."+i);
		}
		for (char i = 'a'; i <= 'z'; i++) {
			System.out.println("for...."+i);
		}
		
		
	}//end main()
	
	
}//end class
