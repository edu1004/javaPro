package test.com;

public class Test01Collection {

	public static void main(String[] args) {
		System.out.println("Collection...");

		// 자바의 자료구조
		// Collection 을 상속받은 List(ArrayList,Vector), Set(HashSet)...
		// Map을 상속받은 HashMap,Hashtable

		// 1.List(index)
		// 배열처리를 함에있어서 배열의 길이를 확정짓지않고
		// 데이터를 추가하는대로 길이가 정해진다.
		// 순서가 있으며, 중복된 데이터를 허용한다.

		// 2.Set(next)
		// 배열처리를 함에있어서 배열의 길이를 확정짓지않고
		// 데이터를 추가하는대로 길이가 정해진다.
		// 순서가 없으며, 중복된 데이터를 허용안한다.

		// 3.Map(key)
		// 배열처리를 함에있어서 배열의 길이를 확정짓지않고
		// 데이터를 추가하는대로 길이가 정해진다.
		// 순서의 개념이 아니고 키와 값의 개념으로 데이터 추가
		// 중복허용

	}

}
